    private void jbtnTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnTotalActionPerformed
        // TODO add your handling code here:
        
        //accessing each array and storing the value of jtxt________. and changing  them to double values
        itemcost[0] =Double.parseDouble(jtxtTea.getText());
        itemcost[1] =Double.parseDouble(jtxtFries.getText());
        itemcost[2] =Double.parseDouble(jtxtCoffee.getText());
        itemcost[3] =Double.parseDouble(jtxtCola.getText());
        itemcost[4] =Double.parseDouble(jtxtOnion.getText());
        itemcost[5] =Double.parseDouble(jtxtChocolate.getText());
        itemcost[6] =Double.parseDouble(jtxtChickenSand.getText());
        itemcost[7] =Double.parseDouble(jtxtChickenSalad.getText());
        itemcost[8] =Double.parseDouble(jtxtCheeseSand.getText());
        itemcost[9] =Double.parseDouble(jtxtHash.getText());
        itemcost[10] =Double.parseDouble(jtxtBottle.getText());
        itemcost[11] =Double.parseDouble(jtxtStrawberry.getText());
        itemcost[12] =Double.parseDouble(jtxtChocolateMilk.getText());
        itemcost[13] =Double.parseDouble(jtxtPan.getText());
        itemcost[14] =Double.parseDouble(jtxtPine.getText());
        itemcost[15] =Double.parseDouble(jtxtVanilla.getText());
        itemcost[16] =Double.parseDouble(jtxtOrange.getText());
        itemcost[17] =Double.parseDouble(jtxtToasted.getText());
        itemcost[18] =Double.parseDouble(jtxtSalad.getText());
        itemcost[19] =Double.parseDouble(jtxtFishSand.getText());
        itemcost[20] =Double.parseDouble(jtxtHamburger.getText());
        
        iChange = Double.parseDouble(jtxtPaymentMethod.getText());
        
        String PaymentMethod = (String) jcmbPaymentMehod.getSelectedItem();// getSelectedItem
        
        if(PaymentMethod == "cash"){
            
        iSubTotal = itemcost[0] + itemcost[1] + itemcost[2] + itemcost[3] + itemcost[4] +itemcost[5] +
                    itemcost[6] + itemcost[7] + itemcost[8] + itemcost[9] + itemcost[10] + itemcost[11] +
                    itemcost[12] + itemcost[13] + itemcost[14] + itemcost[15] + itemcost[16] + itemcost[17] +
                    itemcost[18] + itemcost[19] + itemcost[20] ;
        
        if(iChange >= iSubTotal)
        {    
        iTax = (iSubTotal * Tax_Rate)/100;
        
        String cTax = String.format("$%.2f", iTax); 
        jlblTax.setText(cTax);
        
        String cSubTotal = String.format("$%.2f",iSubTotal);
        jlblSubTotal.setText(cSubTotal);
        
        String cTotal = String.format("$%.2f",iSubTotal + iTax);
        jlblTotal.setText(cTotal);
        
        String cChange = String.format("$%.2f",iChange - (iSubTotal + iTax));
        jlblChange.setText(cChange);
          }
        else{
            JOptionPane.showMessageDialog(null, "Not enough cash", "Fast Food", JOptionPane.OK_OPTION);
        }
        }
        else if(PaymentMethod == "Debit Card" || PaymentMethod == "Master Card" || PaymentMethod == "Visa Card"){
                  
        iSubTotal = itemcost[0] + itemcost[1] + itemcost[2] + itemcost[3] + itemcost[4] +itemcost[5] +
                    itemcost[6] + itemcost[7] + itemcost[8] + itemcost[9] + itemcost[10] + itemcost[11] +
                    itemcost[12] + itemcost[13] + itemcost[14] + itemcost[15] + itemcost[16] + itemcost[17] +
                    itemcost[18] + itemcost[19] + itemcost[20] ;
        
        iTax = (iSubTotal * Tax_Rate)/100;
        
        String cTax = String.format("$%.2f", iTax); 
        jlblTax.setText(cTax);
        
        String cSubTotal = String.format("$%.2f",iSubTotal);
        jlblSubTotal.setText(cSubTotal);
        
        String cTotal = String.format("$%.2f",iSubTotal + iTax);
        jlblTotal.setText(cTotal);
        }
        
    }//GEN-LAST:event_jbtnTotalActionPerformed

