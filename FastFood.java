    private void jtxtFriesKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtxtFriesKeyTyped
        // TODO add your handling code here:
         char NumberOnly = evt.getKeyChar(); //declearing a character numberonly and assigns it to a readable key character.
        if(!(Character.isDigit(NumberOnly))
        || (NumberOnly == KeyEvent.VK_BACKSPACE)
        || (NumberOnly == KeyEvent.VK_DELETE)){
            evt.consume();
        }         
    }//GEN-LAST:event_jtxtFriesKeyTyped

